package nz.ac.canterbury.seng302.tab.entity.enums;

/**
 * Enum for the result of the Activity
 *
 * @WON The team associated with the Activity won
 * @LOSS The team associated with the Activity lost
 * @DRAW The team associated with the Activity drew
 * @UNDECIDED The outcome of the activity is undecided
 */
public enum ActivityResult {
    WON,
    LOSS,
    DRAW,
    UNDECIDED
}